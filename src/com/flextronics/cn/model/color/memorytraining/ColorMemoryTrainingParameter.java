package com.flextronics.cn.model.color.memorytraining;

import com.flextronics.cn.model.ChoosedSample;
import com.flextronics.cn.model.BaseParameter;

/**<b>C-颜色记忆训练</b><br>
 * 参数
 * 
 * @author ZhangGuoYin
 *
 */
public class ColorMemoryTrainingParameter extends BaseParameter {	

	/**
	 * 颜色样本(预留)
	 */
	private ChoosedSample color;
	/**
	 * 显示形体
	 */
	private int[] displayBodys;
	/**
	 * 位元类型
	 */
	private int bitType;
	/**
	 * 开始位元
	 */
	private int startBit;
	/**
	 * 显示模式
	 */
	private int displayMode;
	/**
	 * 显示顺序
	 */
	private int displayOrder;	
	/**
	 * 总题数
	 */
	private int questionCount;

	public ChoosedSample getColor() {
		return color;
	}

	public void setColor(ChoosedSample color) {
		this.color = color;
	}	

	public int[] getDisplayBodys() {
		return displayBodys;
	}

	public void setDisplayBodys(int[] displayBodys) {
		this.displayBodys = displayBodys;
	}

	public int getBitType() {
		return bitType;
	}

	public void setBitType(int bitType) {
		this.bitType = bitType;
	}

	public int getStartBit() {
		return startBit;
	}

	public void setStartBit(int startBit) {
		this.startBit = startBit;
	}

	public int getDisplayMode() {
		return displayMode;
	}

	public void setDisplayMode(int displayMode) {
		this.displayMode = displayMode;
	}

	public int getDisplayOrder() {
		return displayOrder;
	}

	public void setDisplayOrder(int displayOrder) {
		this.displayOrder = displayOrder;
	}

	public int getQuestionCount() {
		return questionCount;
	}

	public void setQuestionCount(int questionCount) {
		this.questionCount = questionCount;
	}
}
